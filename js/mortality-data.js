// Compute life expectancy at a given age for a given gender
// Returns the expected additional years of life
function computeLifeExpectancy(startAge, gender) {
    const table = IBGE_MORTALITY_TABLE[gender];
    let cumSurvival = 1.0;
    let lifeExpectancy = 0;

    for (let age = startAge; age <= 110; age++) {
        const qx = table[Math.min(age, 110)] || 1.0;
        cumSurvival *= (1 - qx);
        lifeExpectancy += cumSurvival;
    }

    return lifeExpectancy;
}
